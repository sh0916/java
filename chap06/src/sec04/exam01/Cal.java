package sec04.exam01;

public class Cal {

	int plus(int x, int y) {
		int result = x + y;
		return result;
	}
	
	int minus(int x, int y) {
		int result = x - y;
		return result;
	}
	
	int multi(int x, int y) {
		int result = x * y;
		return result;
	}
	
	double divide(int x, int y) {
		double result = (double)x / (double)y;
		return result;
	}
	
	double remain(int x, int y) {
		double result = (double)x % (double)y;
		return result;
	}
	
}
